public class swapping {
    public static void main(String[] args) {
        // swapping two numbers  
         int num1 = 3;
         int num2 = 4;

         int c = num1;
         num1=num2;
         num2=c;

         System.out.println("number 1");
         System.out.println(num1);
         System.out.println("number 2");
         System.out.println(num2);


    }
}
